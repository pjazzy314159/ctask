// simple_calculator.h

#ifndef SIMPLE_CALCULATOR_H
#define SIMPLE_CALCULATOR_H

/**
 * Adds two integers.
 * 
 * Parameters:
 * - a: First integer.
 * - b: Second integer.
 * 
 * Returns:
 * - Sum of a and b.
 */
int add(int a, int b);

/**
 * Subtracts the second integer from the first.
 * 
 * Parameters:
 * - a: First integer.
 * - b: Second integer.
 * 
 * Returns:
 * - Result of a - b.
 */
int subtract(int a, int b);

#endif // SIMPLE_CALCULATOR_H
